package edu.mcw.rgd.datamodel.variants;

public class VariantNotes {
    private long rgdId;
    private String notes;

    public long getRgdId() {
        return rgdId;
    }

    public void setRgdId(long rgdId) {
        this.rgdId = rgdId;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }
}
